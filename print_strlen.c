#include "main.h"

/**
 * _strlen - prints length of the string
 * @str: the string
 *
 * Return: Always 0
 */

int _strlen(char *str)
{
int a;

for (a = 0 ; str[a] != '\0' ; a++)
{
return (a);
}
return (a);
}

/**
 * _strlength - prints _strlen
 * @str: the string
 *
 * Return: Always 0
 */

int _strlength(const char *str)
{
int a;

for (a = 0 ; str[a] != '\0' ; a++)
{
return (a);
}
return (a);
}
